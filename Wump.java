public class Wump{
  ////////////////////////////////////////////////////////////////
  // PROPERTIES
  ////////////////////////////////////////////////////////////////
  private boolean ugly;
  
  ////////////////////////////////////////////////////////////////
  // CONSTRUCTORS
  ////////////////////////////////////////////////////////////////
  public Wump(){
    this.ugly = true;
  }
  public Wump(String s){
    if(s.equals("W")){
      this.ugly = true;
    }
    else this.ugly = false;
  }
  
  ////////////////////////////////////////////////////////////////
  // METHODS
  ////////////////////////////////////////////////////////////////
  public boolean isUgly() {
    return ugly;
  }
  public void setUgly(boolean ugly) {
    this.ugly = ugly;
  }
  
}